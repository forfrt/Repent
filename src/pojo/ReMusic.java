package pojo;

import java.sql.Timestamp;

public class ReMusic {

	private int rmcID;//评论歌曲的ID，
	
	private int rmID;//原歌曲的ID
	
	private int rmuID;//评论人的ID
	
	private String rmrContent;//评论的内容
	
	private Timestamp rmrDate;//评论的时间
	
	private int rmrID;//被评论人的ID
	
	private int rmCount;//歌曲被通过的次数
	
	private int rmRCount;//歌曲被评论的次数

	public int getRmcID() {
		return rmcID;
	}

	public void setRmcID(int rmcID) {
		this.rmcID = rmcID;
	}

	public int getRmID() {
		return rmID;
	}

	public void setRmID(int rmID) {
		this.rmID = rmID;
	}

	public int getRmuID() {
		return rmuID;
	}

	public void setRmuID(int rmuID) {
		this.rmuID = rmuID;
	}

	public String getRmrContent() {
		return rmrContent;
	}

	public void setRmrContent(String rmrContent) {
		this.rmrContent = rmrContent;
	}

	public Timestamp getRmrDate() {
		return rmrDate;
	}

	public void setRmrDate(Timestamp rmrDate) {
		this.rmrDate = rmrDate;
	}

	public int getRmrID() {
		return rmrID;
	}

	public void setRmrID(int rmrID) {
		this.rmrID = rmrID;
	}

	public int getRmCount() {
		return rmCount;
	}

	public void setRmCount(int rmCount) {
		this.rmCount = rmCount;
	}

	public int getRmRCount() {
		return rmRCount;
	}

	public void setRmRCount(int rmRCount) {
		this.rmRCount = rmRCount;
	}
	
}
